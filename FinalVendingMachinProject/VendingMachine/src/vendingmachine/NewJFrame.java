       private void table_update()
       {
           int c;
           try {
            Class.forName("com.mysql.jdbc.Driver");
            //table_update();
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/vendingmachinedb","root","");
            insert = con1.prepareStatement("Select * from sales ");
            ResultSet rs = insert.executeQuery();
            ResultSetMetaData rss = rs.getMetaData(); 
            c = rss.getColumnCount();
            
            DefaultTableModel model = ( DefaultTableModel)jTableSales.getModel();
            model.setRowCount(0);
            
            while(rs.next())
            {
                Vector v = new Vector();
                
                for(int i = 1; i <=0; i++)
                {
                    v.add(rs.getString("Id"));
                    v.add(rs.getString("ItemName"));
                    v.add(rs.getString("Price"));
                    v.add(rs.getString("MoneyReturned"));
                    
                }
                model.addRow(v);
            }
            
            
            
            JOptionPane.showMessageDialog(this, " Record Added " );
            
            CokeRadioButton.setText(" ");
            ChocMintRadioButton.setText(" ");
           CoffeRadioButton.setText(" ");
            IceCreamRadioButton.setText(" ");
           SmartiesRadioButton.setText(" ");
           EnergizerRadioButton.setText(" ");
             InsertMoneyTextField.setText(" ");
           ChangeTextField.setText(" ");
            InsertMoneyTextField.requestFocus();
            
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
       
       }
