    private void PurchaseButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PurchaseButton1ActionPerformed
        // This is for our purchase if we have enough money
        
        double money = 0;
        money = Double.parseDouble(InsertMoneyTextField.getText());
        
        if(ChocMintRadioButton.isSelected())
        {
            money = money - 18.00;
        
        }
        else if(CokeRadioButton.isSelected())
        {
            money = money - 20.00;
        }
        else if(IceCreamRadioButton.isSelected())
        {
            money = money - 15.00;
        }
        else if(CoffeRadioButton.isSelected())
        {
            money = money - 8.00;
        }
        else if(SmartiesRadioButton.isSelected())
        {
            money = money - 10.00;
        }
        else if(EnergizerRadioButton.isSelected())
        {
            money = money - 25.00;
        }
        
        if(money < 0)
        {
            JOptionPane.showMessageDialog(rootPane, " Sorry you do not have enough money ");
        }
        else
        {
            ChangeTextField.setText(Double.toString(money));
        }
    }//GEN-LAST:event_PurchaseButton1ActionPerformed

